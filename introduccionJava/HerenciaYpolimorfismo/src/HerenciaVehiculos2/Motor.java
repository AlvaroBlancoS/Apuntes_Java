package HerenciaVehiculos2;

public class Motor {
	
	String modelo;
	int caballos;
	double parNm;
	int numCilindros;
	
	public Motor() {
		
	}

	public Motor(String modelo, int caballos, double parNm, int numCilindros) {
		super();
		this.modelo = modelo;
		this.caballos = caballos;
		this.parNm = parNm;
		this.numCilindros = numCilindros;
	}
	
	
	
}
